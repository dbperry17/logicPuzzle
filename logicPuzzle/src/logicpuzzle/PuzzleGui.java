    /**
     * clickOnKey - Allows for a JButton to be used via a keyboard shortcut
     * 
     * Code found at: http://stackoverflow.com/questions/4240740/shortcut-key-for-jbutton-without-using-alt-key
     * 
     * @param button	    Button to be used
     * @param actionName    Name of action performed
     * @param key	    Key on keyboard used to trigger the action
     * 
     */
    public static void clickOnKey(final AbstractButton button, String actionName, int key)
    {
	button.getInputMap(JButton.WHEN_IN_FOCUSED_WINDOW)
	    .put(KeyStroke.getKeyStroke(key, 0), actionName);

	button.getActionMap().put(actionName, new AbstractAction()
	{
	    @Override
	    public void actionPerformed(ActionEvent e)
	    {
		button.doClick();
	    }
	});
    }
