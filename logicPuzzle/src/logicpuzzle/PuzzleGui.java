    /**
     * getIndices - finds i, j, and k in myGrid[i][j][k] for button in main grid
     *		    i = grid section
     *		    j = row
     *		    k = column
     *
     * @param myButton - Button user wants index of
     *
     * @return index - Size three int array of [i, j, k]
     */
    private int[] getIndices(ColoredJButton myButton)
    {
	outerloop:
	for(int i = 0; i < myGrid.length; i++)
	    for(int j = 0; j < myGrid[i].length; j++)
		for(int k = 0; k < myGrid[i][j].length; k++)
		    if(myGrid[i][j][k] == myButton)
		    {
			lastPos[0] = i;
			lastPos[1] = j;
			lastPos[2] = k;
			break outerloop;
		    }
	
	return lastPos;
    }
