    /**
     * greenChange - Turns the specified button green, and changes all other buttons in the same
     *		     row and column of the specified button to red.
     * 
     * @param myButton - The button being changed to green
     */
    private void greenChange(ColoredJButton myButton)
    {
	int[] temp = getIndices(myButton);
	
	//change row to red
	for(int i = 0; i < myGrid[temp[0]][temp[1]].length; i++)
	{
	    lastRowStatus[i] = myGrid[temp[0]][temp[1]][i].getStatus();
	    myGrid[temp[0]][temp[1]][i].setColor(1);
	}
	
	//change column to red
	for(int i = 0; i < myGrid[temp[0]].length; i++)
	{
	    lastColStatus[i] = myGrid[temp[0]][i][temp[2]].getStatus();
	    myGrid[temp[0]][i][temp[2]].setColor(1);
	}
	myButton.setColor(3);
	// FIXME: Changing Brit-Red to green causes Dane-White to change to red.
    }
