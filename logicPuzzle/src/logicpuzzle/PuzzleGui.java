    /**
     * reverseChange - Undo actions of greenChange();
     * 
     * @param myButton - The button that is currently green
     */
    private void reverseChange(ColoredJButton myButton)
    {
	int[] temp = getIndices(myButton);
	
	//change row to previous color
	for(int i = 0; i < myGrid[temp[0]][temp[1]].length; i++)
	    myGrid[temp[0]][temp[1]][i].setColor(lastRowStatus[i]);
	//change column to red
	for(int i = 0; i < myGrid[temp[0]].length; i++)
	    myGrid[temp[0]][i][temp[2]].setColor(lastColStatus[i]);
	myButton.setColor(2);
    }
