    /**
     * Actions of all buttons
     */
    private class MainButtonAction extends AbstractAction
    {
	/**
	 * actionPerformed - Actions for when a button is clicked
	 *
	 * @param e
	 */
	@Override
	public void actionPerformed(ActionEvent e)
	{
	    //Undo Button: Undo last action(s)
	    if(e.getSource() == ButtonSystemUndo)
	    {	
		switch (lastButton.getStatus())
		{
		//If button is green
		    case 3:
			reverseChange(lastButton);
			break;
		//If button is grey
		    case 0:
			greenChange(lastButton);
			break;
		    default:
			lastButton.setPrevColor();
			break;
		}

	    ButtonSystemUndo.setEnabled(false);
	    ButtonSystemRedo.setEnabled(true);
	    }
	    //Redo Button: Redo undone action(s)
	    else if(e.getSource() == ButtonSystemRedo)
	    {
		switch (lastButton.getStatus())
		{
		//If button is yellow
		    case 2:
			greenChange(lastButton);
			break;
		    default:
			lastButton.setNextColor();
			break;
		}

		ButtonSystemRedo.setEnabled(false);
		ButtonSystemUndo.setEnabled(true);
	    }
	    //Clear Button: Make all buttons grey
	    else if(e.getSource() == ButtonSystemClear)
	    {
		if(started)
		{
		    int selectedOption = JOptionPane.showConfirmDialog(null,
				      "Are you sure you wish to clear the board?\n"
					      + "You won't be able to undo this action!",
				      "Clear board?",
				      JOptionPane.YES_NO_OPTION);
		    if (selectedOption == JOptionPane.YES_OPTION)
		    {
			clearGrid();
		    }
		}
		else
		    clearGrid();
	    }
	    //Default Button: Implement initial clues
	    else if(e.getSource() == ButtonSystemDefault)
	    {
		int selectedOption;
		if(started)
		{
		    selectedOption = JOptionPane.showConfirmDialog(null,
					 "Are you sure you wish to reset the board?\n"
						 + "You won't be able to undo this action!",
					 "Reset board?",
					 JOptionPane.YES_NO_OPTION);
		    if (selectedOption == JOptionPane.YES_OPTION)
			resetButton();
		}
		else
		    resetButton();
		    
	    }
	    //Close Button
	    else if(e.getSource() == ButtonSystemClose)
	    {
		if(!testing)
		{
		    int selectedOption = JOptionPane.showConfirmDialog(null,
				      "Are you sure you wish to close the window?",
				      "Close window?",
				      JOptionPane.YES_NO_OPTION);
		    if (selectedOption == JOptionPane.YES_OPTION)
			System.exit(0);
		}
		else
		    System.exit(0);
	    }
	    //Grid Button: Change color
	    else if(e.getSource() instanceof ColoredJButton)
	    {
		//For Undo Feature
		lastButton = (ColoredJButton)e.getSource();
		
		if(((ColoredJButton)e.getSource()).getStatus() == 2) //if button is yellow
		    greenChange((ColoredJButton)e.getSource());
		else
		    ((ColoredJButton)e.getSource()).setNextColor();
		
		ButtonSystemUndo.setEnabled(true);
		ButtonSystemRedo.setEnabled(false);
		started = true;
	    }
	}
    }
